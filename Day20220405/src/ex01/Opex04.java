package ex01;

public class Opex04 {

	public static void main(String[] args) {
		
		int x = 10;
		int y = 8;
		
		System.out.println(x / y);  //몫 소수점 날라감
		System.out.println(x % y);	//나머지값이 나옴
		System.out.println(x % -y); //부호를 넣어도 나머지 값이라 부호 무시댐
		
		System.out.println(x > y); //
		System.out.println(x < y); //
		System.out.println(x >= y); // <=(o) =<(x) 등호 준서 지켜야함
		System.out.println(x <= y); //
		
		System.out.println(x == y); // 
		System.out.println(x != y); // 
		
		
		

	}

}
